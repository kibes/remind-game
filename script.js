function playSound(soundName) {
    if (!state.audioEnabled || !state.soundsLoaded) {
        console.log("–ê—É–¥–∏–æ –æ—Ç–∫–ª—é—á–µ–Ω–æ –∏–ª–∏ –Ω–µ –∑–∞–≥—Ä—É–∂–µ–Ω–æ:", soundName);
        return false;
    }
    
    const sound = audio[soundName];
    if (!sound) {
        console.warn("–ó–≤—É–∫ –Ω–µ –Ω–∞–π–¥–µ–Ω:", soundName);
        return false;
    }
    
    const now = Date.now();
    const timeSinceLastPlay = now - state.lastPlayTime[soundName];
    
    // –ú–∏–Ω–∏–º–∞–ª—å–Ω–∞—è –∑–∞–¥–µ—Ä–∂–∫–∞ –º–µ–∂–¥—É –æ–¥–∏–Ω–∞–∫–æ–≤—ã–º–∏ –∑–≤—É–∫–∞–º–∏
    if (timeSinceLastPlay < 50) {
        return false;
    }
    
    if (state.audioLock[soundName]) {
        return false;
    }
    
    state.audioLock[soundName] = true;
    state.lastPlayTime[soundName] = now;
    
    try {
        // –í—Å–µ–≥–¥–∞ —Å–±—Ä–∞—Å—ã–≤–∞–µ–º –≤—Ä–µ–º—è
        sound.currentTime = 0;
        
        // –î–ª—è –Ω–µ–∫–æ—Ç–æ—Ä—ã—Ö —É—Å—Ç—Ä–æ–π—Å—Ç–≤ –º–æ–∂–µ—Ç –ø–æ—Ç—Ä–µ–±–æ–≤–∞—Ç—å—Å—è –ø–µ—Ä–µ–∑–∞–≥—Ä—É–∑–∫–∞
        if (sound.readyState === 0) {
            sound.load();
        }
        
        // –ü—Ä–æ–±—É–µ–º –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ—Å—Ç–∏
        const playPromise = sound.play();
        
        if (playPromise !== undefined) {
            playPromise.then(() => {
                // –°–Ω–∏–º–∞–µ–º –±–ª–æ–∫–∏—Ä–æ–≤–∫—É –ø–æ—Å–ª–µ –∑–∞–≤–µ—Ä—à–µ–Ω–∏—è
                sound.onended = () => {
                    state.audioLock[soundName] = false;
                };
                
                // –ù–∞ –≤—Å—è–∫–∏–π —Å–ª—É—á–∞–π —Å–Ω–∏–º–∞–µ–º –±–ª–æ–∫–∏—Ä–æ–≤–∫—É —á–µ—Ä–µ–∑ –≤—Ä–µ–º—è
                setTimeout(() => {
                    state.audioLock[soundName] = false;
                }, 1000);
                
            }).catch(error => {
                console.warn(`–ù–µ —É–¥–∞–ª–æ—Å—å –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ—Å—Ç–∏ ${soundName}:`, error);
                state.audioLock[soundName] = false;
                
                // –ü—Ä–æ–±—É–µ–º —Ñ–æ–ª–±—ç–∫
                if (state.forceAudioFallback) {
                    setTimeout(() => {
                        try {
                            const fallbackSound = new Audio(sound.src);
                            fallbackSound.volume = sound.volume;
                            fallbackSound.play().catch(() => {});
                        } catch (e) {
                            console.warn("–§–æ–ª–±—ç–∫ –Ω–µ —Å—Ä–∞–±–æ—Ç–∞–ª:", e);
                        }
                    }, 10);
                }
            });
        } else {
            state.audioLock[soundName] = false;
        }
        
        return true;
    } catch (error) {
        console.warn(`–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–æ–ø—ã—Ç–∫–µ –≤–æ—Å–ø—Ä–æ–∏–∑–≤–µ—Å—Ç–∏ ${soundName}:`, error);
        state.audioLock[soundName] = false;
        return false;
    }
}

